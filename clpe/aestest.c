static char *
hexstr(unsigned char *data, size_t len)
{
  static char buf[1024];
  char *p;
  int i;
  if (len * 2 + 1 > sizeof(buf)) {
    fprintf(stderr, "%s: %d: too long data\n", __FILE__, __LINE__);
    abort();
  }
  for (i = 0, p = buf; i < len; i++, p += 2) {
    sprintf(p, "%02X", data[i] & 0xff);
  }

  return buf;
}

static void
shift_right(unsigned char *data, size_t len)
{
  int i, c, nc;
  c = 0;
  for (i = 0; i < len; i++) {
    nc = (data[i] & 1);
    data[i] = (data[i] >> 1) | (c ? 0x80 : 00);
    c = nc;
  }
}

static int
ecb_vk_enc(int verbose)
{
  unsigned char key[16], pt[16], ct[16];
  clpe_aes_ctx_t ctx;
  int nerrs;
  int i;

  memset(pt, 0, sizeof(pt));
  memset(key, 0, sizeof(key));
  key[0] = 0x80;

  nerrs = 0;

  if (verbose) {
    printf("\n=========================\n\n");
    printf("Electronic Codebook (ECB) Mode\n");
    printf("Variable Key Known Answer Tests\n");
    printf("\n==========\n\n");
    printf("KEYSIZE=%d\n\n", sizeof(key) * 8);
    printf("PT=%s\n\n", hexstr(pt, sizeof(pt)));
  }

  for (i = 0; i < 128; i++) {
    memset(&ctx, 0, sizeof(ctx));
    clpe_aes_enc_key(key, sizeof(key), &ctx);
    clpe_aes_enc_blk(pt, ct, &ctx);

    if (verbose) {
      printf("I=%d\n", i + 1);
      printf("KEY=%s\n", hexstr(key, sizeof(key)));
      printf("CT=%s\n", hexstr(ct, sizeof(ct)));
    }

    if (memcmp(ecb_vk_128[i], ct, sizeof(ct)) != 0) {
      nerrs++;
      if (verbose)
	printf("*** error: correct CT=%s\n",
	       hexstr(ecb_vk_128[i], sizeof(ct)));
    }

    if (verbose)
      printf("\n");

    shift_right(key, sizeof(key));
  }

  return nerrs == 0;
}

