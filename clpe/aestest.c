int
dotest(int (*func)(int), char *desc, int verbose, int debug)
{
  int result;

  if (verbose) {
    if (debug) {
      printf("=========================\n");
      printf("%s\n", desc);
      printf("=========================\n");
    } else {
      printf("%s ... ", desc);
      fflush(stdout);
    }
  }

  result = (*func)(debug);

  if (verbose) {
    if (debug) {
      printf("\n=========================\n");
      printf("%s\n", desc);
      printf("RESULT: %s\n", result ? "OK" : "ERROR");
      printf("=========================\n\n");
    } else {
      printf("%s\n", result ? "ok" : "ERROR");
    }
  }

  return result;
}

int
main(int argc, char **argv)
{
  int verbose, debug;
  int result, errorcount;
  char *progname;

  progname = argv[0];

  verbose = debug = 0;
  while (argc > 1) {
    if (strcmp(argv[1], "-d") == 0)
      debug = 1;
    else if (strcmp(argv[1], "-v") == 0)
      verbose = 1;
    else {
      fprintf(stderr, "Usage: %s [-d][-v]\n", progname);
      exit(2);
    }
    argc--;
    argv++;
  }

  if (!dotest(ecb_vk_enc, "ECB Mode / Variable Key / Encryption",
	      verbose, debug))
    errorcount++;
  if (!dotest(ecb_vk_dec, "ECB Mode / Variable Key / Decryption",
	      verbose, debug))
    errorcount++;
  if (!dotest(ecb_vt_enc, "ECB Mode / Variable Text / Encryption",
	      verbose, debug))
    errorcount++;
  if (!dotest(ecb_vt_dec, "ECB Mode / Variable Text / Decryption",
	      verbose, debug))
    errorcount++;

  return errorcount ? 1 : 0;
}
