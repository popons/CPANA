static char *
hexstr(unsigned char *data, size_t len)
{
  static char buf[1024];
  char *p;
  int i;
  if (len * 2 + 1 > sizeof(buf)) {
    fprintf(stderr, "%s: %d: too long data\n", __FILE__, __LINE__);
    abort();
  }
  for (i = 0, p = buf; i < len; i++, p += 2) {
    sprintf(p, "%02X", data[i] & 0xff);
  }

  return buf;
}

static int
eax_aes128_enc(int verbose)
{
  clpe_eax_t *ctx;
  int nerrs, i;
  clpe_aes_ctx_t aes_ctx;
  uint8_t text[64];

  nerrs = 0;

  memset(&aes_ctx, 0, sizeof(aes_ctx));

  ctx = clpe_eax_new(CLPE_AES_BLOCK_SIZE,
		     (clpe_blkciph_encrypt_func_t)clpe_aes_enc_blk,
		     (void *)&aes_ctx);
  if (ctx == NULL)
    return 0;

  for (i = 0; i < EAX_TEST_VECTOR_SIZE; i++) {
    clpe_aes_enc_key(test_vector[i].key, 16, &aes_ctx);	/* 128 bit key */
    clpe_eax_init(ctx, CLPE_OMAC1); /* indicate that key's been changed */

    clpe_eax_add_nonce(ctx, test_vector[i].nonce, 16);
    clpe_eax_add_header(ctx, test_vector[i].header, 8);
    clpe_eax_encrypt(ctx, test_vector[i].msg, test_vector[i].msglen, text);
    clpe_eax_final(ctx, text + test_vector[i].msglen, CLPE_AES_BLOCK_SIZE);

    if (verbose) {
      printf("\n");
      printf("MSG:    %s\n",
	     hexstr(test_vector[i].msg, test_vector[i].msglen));
      printf("KEY:    %s\n",
	     hexstr(test_vector[i].key, sizeof(test_vector[i].key)));
      printf("NONCE:  %s\n",
	     hexstr(test_vector[i].nonce, sizeof(test_vector[i].nonce)));
      printf("HEADER: %s\n",
	     hexstr(test_vector[i].header, sizeof(test_vector[i].header)));
      printf("CIPHER: %s\n",
	     hexstr(text, test_vector[i].msglen + CLPE_AES_BLOCK_SIZE));
    }
    if (memcmp(text, test_vector[i].cipher,
	       test_vector[i].msglen + CLPE_AES_BLOCK_SIZE) != 0) {
      if (verbose)
	printf("*** error found ***\n");
      nerrs++;
    }
  }

  clpe_eax_destroy(ctx);
  
  return nerrs == 0;
}

int
dotest(int (*func)(int), char *desc, int verbose, int debug)
{
  int result;

  if (verbose) {
    if (debug) {
      printf("=========================\n");
      printf("%s\n", desc);
      printf("=========================\n");
    } else {
      printf("%s ... ", desc);
      fflush(stdout);
    }
  }

  result = (*func)(debug);

  if (verbose) {
    if (debug) {
      printf("\n=========================\n");
      printf("%s\n", desc);
      printf("RESULT: %s\n", result ? "OK" : "ERROR");
      printf("=========================\n\n");
    } else {
      printf("%s\n", result ? "ok" : "ERROR");
    }
  }

  return result;
}

int
main(int argc, char **argv)
{
  int verbose, debug;
  int result, errorcount;
  char *progname;

  progname = argv[0];

  verbose = debug = 0;
  while (argc > 1) {
    if (strcmp(argv[1], "-d") == 0)
      debug = 1;
    else if (strcmp(argv[1], "-v") == 0)
      verbose = 1;
    else {
      fprintf(stderr, "Usage: %s [-d][-v]\n", progname);
      exit(2);
    }
    argc--;
    argv++;
  }

  if (!dotest(eax_aes128_enc, "EAX-AES128 Encryption",
	      verbose, debug))
    errorcount++;

  return errorcount ? 1 : 0;
}
